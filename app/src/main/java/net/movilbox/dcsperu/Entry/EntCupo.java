public class EntCupo {

    private int idBode;
    private int idReferencia;
    private double totalRef;
    private int tipo_bodega;

    public int getIdBode() {
        return idBode;
    }

    public void setIdBode(int idBode) {
        this.idBode = idBode;
    }

    public int getIdReferencia() {
        return idReferencia;
    }

    public void setIdReferencia(int idReferencia) {
        this.idReferencia = idReferencia;
    }

    public double getTotalRef() {
        return totalRef;
    }

    public void setTotalRef(double totalRef) {
        this.totalRef = totalRef;
    }

    public int getTipo_bodega() {
        return tipo_bodega;
    }

    public void setTipo_bodega(int tipo_bodega) {
        this.tipo_bodega = tipo_bodega;
    }


}
